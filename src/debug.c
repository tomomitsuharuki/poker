/**
 * @brief	デバッグ出力
 * @note	printfラッパー
 *
 * @param[in]	出力文字列
 */
void debug_printf(const char *format, ...)
{
	va_list va;
	va_start(va, format);
	vprintf(format, va);
	va_end(va);
}


