/**
 * @brief	乱数生成初期化
 * @note	
 * @attention
 * system_random_init()呼び出し前に一度呼び出ししてください
 *
 */
void system_random_init(void)
{
	M_ENTRY();
	srand((unsigned)time(NULL));
}

/**
 * @brief	乱数生成
 * @note	int型の乱数を出力します
 * @attention
 * system_random_init()呼び出し後に呼び出してください
 *
 */
int system_random(void)
{
	M_ENTRY();
	int r = rand();
	return r;
}
