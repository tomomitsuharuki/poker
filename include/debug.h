/** @def
 * エラー出力マクロ
 */
#define M_DEBUG_ERROR(...) \
{ \
	printf("[ERROR:%d:%s(%s)] " __VA_ARGS__ "\n", __LINE__, __FUNCTION__, __FILE__); \
}

/** @def
 * エントリートレースマクロ
 */
#define M_DEBUG_ENTRY(...) \
{ \
	printf("[ENTRY:%d:%s(%s)] " __VA_ARGS__ "\n", __LINE__, __FUNCTION__, __FILE__); \
}

